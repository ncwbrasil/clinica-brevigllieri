                            	<p class="titulo"> TABAGISMO</p>
                                <p>O tabagismo é considerado pela Organização Mundial da Saúde (OMS) a principal causa de morte evitável no mundo. A organização estima que um terço da população mundial adulta, isto é, cerca de 1 bilhão e 200 milhões de pessoas, sejam fumantes. Pesquisas comprovam que aproximadamente 47% de toda a população masculina mundial e 12% da feminina fumam. 
                                <p>A fumaça do cigarro tem mais de 4,7 mil substâncias tóxicas. O alcatrão, por exemplo, é composto de mais de 40 compostos cancerígenos. Já o monóxido de carbono (CO), em contato com a hemoglobina do sangue, dificulta a oxigenação e, consequentemente, ao privar alguns órgãos do oxigênio, causa doenças como a aterosclerose (que obstrui os vasos sanguíneos). A nicotina é considerada pela Organização Mundial da Saúde (OMS) uma droga psicoativa que causa dependência. Ela também aumenta a liberação de catecolaminas, que contraem os vasos sanguíneos e aceleram a frequência cardíaca, causando hipertensão arterial.
                                <p>O tabagismo está relacionado a mais de 50 doenças, sendo responsável por 30% das mortes por câncer de boca, 90% das mortes por câncer de pulmão, 25% das mortes por doença do coração, 85% das mortes por bronquite e enfisema e 25% das mortes por derrame cerebral. Segundo a Organização Mundial da Saúde (OMS), todo ano cinco milhões de pessoas morrem no mundo por causa do cigarro. E, em 20 anos, esse número chegará a 10 milhões, se o consumo de produtos como cigarros, charutos e cachimbos continuar aumentando.
                                <p>Segundo o Instituto Nacional do Câncer (INCA), o tabaco também tem relação com a impotência sexual e infertilidade masculina, pois, segundo estudos, prejudica a mobilidade do espermatozoide. Os mesmos prejuízos também são atribuídos ao cachimbo e ao charuto. Apesar de não serem tragáveis, possuem uma concentração de nicotina maior, que é absorvida pela mucosa oral.
                                <p>Muito difundido entre os jovens, o narguilé é uma espécie de cachimbo de água perfumada. Apesar de parecer inofensivo, as essências geralmente são compostas por uma mistura de tabaco com frutas ou aromatizantes. De acordo com a Organização Mundial da Saúde (OMS), uma hora de sessão de narguilé equivale a fumar cem cigarros comuns.

                                <p>Embora tenha a mesma quantidade de substâncias tóxicas – 4.700, segundo o Instituto Nacional do Câncer – uma sessão com o instrumento dura mais do que tragar um cigarro inteiro. O narguilé pode ter uma ou mais tubulações por onde o fumo chega até a boca do usuário – o que o torna compartilhável.
                                <p>Além dos riscos para a saúde relacionados ao tabaco e ao fumo passivo, os especialistas ressaltam a possibilidade da transmissão de doenças infecciosas devido ao compartilhamento da piteira, como hepatite, herpes e tuberculose.
                                <p>Não só o fumo ativo, mas o passivo também aumenta os riscos de doença. Sete não fumantes morrem por dia em consequência do fumo passivo. O tabagismo passivo aumenta em 30% o risco para câncer de pulmão e 24% o risco para infarto.
                                <p><b>Tratamento</b></p>
								<p>Os tratamentos mais eficazes unem apoio medicamentoso com mudanças de hábitos. A combinação é importante, porque o tabaco causa dependência física, psicológica e comportamental.
                                <p>O uso de medicações pode duplicar, ou até triplicar, o resultado do tratamento para cessação do tabagismo. A maioria dos fumantes não consegue parar de fumar sozinho e procurar um médico para o tratamento aumenta consideravelmente a possibilidade de sucesso.

