                                <p class="titulo">DPOC</p>
                                <p>A Doença Pulmonar Obstrutiva Crônica, também conhecida como “enfisema” ou “bronquite do fumante”, é uma doença crônica que está relacionada à exposição de gases nocivos (tabagismo, exposições ocupacionais e ambientais).
                                <p>A suspeita deve ser feita principalmente em indivíduos com 40 anos ou mais com antecedente de tabagismo (atual ou pregresso) e sintomas respiratórios. Inicialmente os pacientes apresentam falta de ar ao realizar esforços e tosse intermitente (muitas vezes mencionada como “pigarro”), que quando frequente, são atribuídos à idade ou ao hábito de fumar, no entanto podem representar o início da DPOC. O diagnóstico da DPOC é determinado pelo exame de função pulmonar (Espirometria) que deve ser solicitado a todos os fumantes independente da presença de sintomas respiratórios significativos.
                                <p>Com a evolução da DPOC, os sintomas diários se intensificam e podem acontecer exacerbações (“crises”) que limitam muito a qualidade de vida e podem culminar com internações hospitalares, inclusive podendo ser fatal.
                                <p>Apesar de crônica e parcialmente, a DPOC possui tratamento. A terapia medicamentosa é feita preferencialmente por via inalatória e visa a redução dos sintomas e melhora da qualidade de vida e a prevenção das exacerbações.
                                <p>A principal medida na DPOC, no entanto, não é medicamentosa. A intervenção mais impactante é, sem dúvida, a cessação do tabagismo! Além da terapia medicamentosa e cessação do tabagismo, a prática de atividades físicas aeróbicas regulares (150 minutos por semana) também têm papel importante na evolução da doença.
