                                <p class="titulo">ASMA</p>
                                <p>Popularmente conhecida como “bronquite”, a asma é uma doença atópica (alérgica), com componente genético/hereditário (é frequente a presença de parentes próximos com antecedente de asma ou doenças alérgicas como a rinite e a dermatite), crônica (não curável) e completamente reversível sob tratamento adequado.
                                <p>A asma é muito prevalente no Brasil, acometendo de 10 a 15% da população. Comumente inicia-se durante a infância e apresenta-se clinicamente como falta de ar, chiado no peito, tosse ou sensação de aperto no peito ou dificuldade inespecífica para respirar.
                                <p>É fundamental o acompanhamento com o especialista, pois a asma é uma doença crônica e o não tratamento adequado implica em impacto significativo na qualidade de vida, muitas vezes impedindo a realização de atividades de lazer, exercícios físicos e, até mesmo, atividades cotidianas.
                                <p>O tratamento é fundamentalmente feito por via inalatória e as medicações preventivas são absorvidas em sua grande maioria pelos pulmões, apresentando poucos efeitos adversos. O uso equivocado de medicações não orientadas por especialistas pode implicar em um não controle, agravamento da doença e efeitos colaterais à curto e longo prazo.
